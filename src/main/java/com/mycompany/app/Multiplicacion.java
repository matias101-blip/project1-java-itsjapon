package com.mycompany.app;
import java.lang.Math;

import java.io.*;

public class Multiplicacion {
	
	public static void main (String[] args) {
		//Defino variables
		int n1, cont, mult;
		//Entrada
		n1=3;
		//For o para
		for (cont=1;cont<=12;cont++)
		{
			mult=n1*cont;
			System.out.println(n1+"*"+cont+"="+mult);
			}
	}
}

