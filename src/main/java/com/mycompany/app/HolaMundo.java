public class HolaMundo {
	
	public static void main (String[] args) {
		//Declaro variables ,inicializo y entrada
		int n1,n2;
		int sum;
		
		//Entrada
		n1=5;
		n2=20;
		
		//Proceso
		sum=n1+n2;
		
		//Salida
		System.out.println("Se suma " + n1 + " + " + n2);
		System.out.println("El resultado de la suma es: "+sum);
		
	}
}

